						<b>Last update</b><br/>
					A major issue for some plugins is the latest update date. On June 2015 we counted 17,000 plugins (!!!) that hadn't been updated for more than 2 years. Chances are that those plugins have been abandoned by the developer. It's not an exact science, but it's worth having this notion in mind when choosing a plugin or a theme. 					 
					 </p>
					<p>						
						<b>Plugin Type (aka Premium vs Standard)</b><br/>

					One important question when searching for a plugin/theme is the following: who's behind this plugin? is it a developer who built this as a hobby or is there a company behind it?
					This question might seem irrelevant for some cases, but if you want to get an indication of whether the plugin/theme will be supported in the future it's a crucial one.

					There's no such a thing as a standard definition of what a premium plugin/theme is, so we decided to make our own.
					<br/>This is in a nutshell what classifies a plugin/theme as Premium:
					  </p>
